def is_monotone(a : int, b : int) -> bool:
    # Can `a` --> `b` without any bits in `a` transitioning from `0` --> `1`?
    # If so, then `b` is reachable from `a` without an erase, and we can stay in the same chain.
    # Otherwise, we need to start a new chain (which corresponds to doing an erase).
    return (a & b) == b
def popcount(x : int) -> int:
    return POPCOUNT_TABLE[x & 0xFF]

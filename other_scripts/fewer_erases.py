def build_popcount_table():
    table = [0] * 256
    for i in range(256):
        table[i] = bin(i).count("1")
    return table
def generate_flash_walk_via_computation():
    # Prepare ordering: highest popcount first, then numeric order
    order = list(range(256))
    order.sort(key=lambda v: (-popcount(v), v))

    # Track values already used in the sequence
    used = [False] * 256
    seq : list[int] = []

    # How many chains will be needed?
    chains_required = 0

    for start in order:
        if used[start]:
            continue

        chains_required += 1
        cur = start
        while True:
            seq.append(cur)
            used[cur] = True

            # Try clearing one more bit (bit order 0..7)
            next_val = None
            for bit in range(8):
                if cur & (1 << bit):
                    cand = cur & ~(1 << bit)
                    if not used[cand]:
                        next_val = cand
                        break

            if next_val is None:
                break

            cur = next_val

    # Sanity check: must contain all 256 distinct values
    if len(seq) != 256 or len(set(seq)) != 256:
        raise RuntimeError("Sequence generation failed")

    print(f"Chains required: {chains_required}")
    return bytes(seq)
